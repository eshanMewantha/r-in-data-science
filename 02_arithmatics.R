value.one <- 25
value.two <- 5

summation <- value.one + value.two
print(summation)

difference <- value.one - value.two
print(difference)

multiplication <- value.one * value.two
print(multiplication)

division <- value.one / value.two
print(division)

exponential.method.one <- value.one ^ value.two
print(exponential.method.one)

exponential.method.two <- value.one ** value.two
print(exponential.method.two)

modulus <- value.one %% value.two
print(modulus)

integer.division <- value.one %/% value.two
print(integer.division)

